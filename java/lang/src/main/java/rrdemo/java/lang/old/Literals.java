package rrdemo.java.lang.operators;

/**
 * 字面量.
 *
 * @author zhengrr
 * @since 2016-9-9
 */
public final class Literals {
   /**
    * 构造方法.
    */
   private Literals() { }

   /**
    * 布尔型.
    */
   public static final boolean BOOLFALSE = false;

   /**
    * 布尔型.
    */
   public static final boolean BOOLTRUE = true;

   /**
    * 字符型.
    */
   public static final char CHAR = '\uffff';

   /**
    * 字节整型.
    */
   public static final byte BYTE8BITS = (byte) 127;

   /**
    * 短整型.
    */
   public static final short SHORT16BITS = (short) 35535;

   /**
    * (十进制)整型.
    */
   public static final int INT32BITS = 2147483647;

   /**
    * 八进制整型.
    */
   public static final int INT_OCTAL = 037777777777;

   /**
    * 十六进制整型.
    */
   public static final int INT_HEXADECIMAL = 0xffffffff;

   /**
    * 长整型.
    */
   public static final long LONG64BITS = 9223372036854775807L;

   /**
    * (单精度)浮点型.
    */
   public static final float FLOAT = 0.0f;

   /**
    * 双精度浮点型.
    */
   public static final double DOUBLE = 0.0d;
}
